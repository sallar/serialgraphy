    $abbr   = split(" ", $items[0]);
    $abbr   = $abbr[0];
    $offset = pad(filter_var($items[count($items) - 1], FILTER_SANITIZE_NUMBER_INT, FILTER_SANITIZE_SPECIAL_CHARS));
    $abbrs[$abbr] = $offset;
}

function pad($str)
{
    if( $str == "" ) return "+0000";

    $sign = substr($str, 0, 1);
    $num  = substr($str, 1);

    if( strlen($num) == 3 ) return $sign . "0" . $num;
    elseif( strlen($num) == 2 ) return $sign . $num . "00";
    elseif( strlen($num) == 1 ) return $sign . "0" . $num . "00";
    else return $str;
}
